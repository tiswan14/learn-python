a = 4
b = 2

#Lebih dari
print ("\n=======Lebih dari (>)")
hasil = a > 3
print (a, '>', 3 ,'=', hasil)

hasil = b > 3
print (b, '>', 3 ,'=', hasil)

hasil = b > 2
print (b, '>', 2 ,'=', hasil)

#Kurang dari
print ("\n=======Kurang dari (<)")

hasil = a < 3
print (a, '<', 3 ,'=', hasil)

hasil = b < 3
print (b, '<', 3 ,'=', hasil)

hasil = b < 2
print (b, '<', 2 ,'=', hasil)

#Lebih dari sama dengan
print ("\n=======Lebih dari sama dengan(>=)")
hasil = a >= 3
print (a, '>=', 3 ,'=', hasil)

hasil = b >= 3
print (b, '>=', 3 ,'=', hasil)

hasil = b >= 2
print (b, '>=', 2 ,'=', hasil)

#Kurang dari sama dengan
print ("\n=======Kurang dari sama dengan(<=)")

hasil = a <= 3
print (a, '<=', 3 ,'=', hasil)

hasil = b <= 3
print (b, '<=', 3 ,'=', hasil)

hasil = b <= 2
print (b, '<=', 2 ,'=', hasil)

print ("\n=======Sama dengan(==)")
hasil = a == 4
print (a, "==", 4, '=', hasil) 
hasil = b == 4
print (b, "==", 4, '=', hasil) 

print ("\n=======Tidak Sama dengan(!=)")
hasil = a != 4
print (a, "!=", 4, '=', hasil) 
hasil = b != 4
print (b, "!=", 4, '=', hasil) 

print ("\n=======Object Identity(is)")
x = 4
hasil = x is 4
print (x, 'is', 4,'=', hasil)

hasil = x is 5
print (x, 'is', 5,'=', hasil)

print ("\n=======Object Identity(is not)")
hasil = x is not 4
print (x, 'is not', 4,'=', hasil)

hasil = x is not 5
print (x, 'is not', 5,'=', hasil)
