# Operator Bitwise
c = True
# OR
print("\nNilai c:", c)
c |= False
print("Nilai c |= False sekarang:", c)
c = False
print("Nilai c = False sekarang:", c)
c |= False
print("Nilai c| = False sekarang:", c)

# AND
c &= False
print("\nNilai c &= False sekarang:", c)
c = True
print("Nilai c = True sekarang:", c)
c &= True
print("Nilai c& = True sekarang:", c)

# XOR
c ^= False
print("\nNilai c ^= False sekarang:", c)
c = True
print("Nilai c = True sekarang:", c)
c ^= True
print("Nilai c^ = True sekarang:", c)
