# Kumpulan data number

data_angka = [1, 2, 3]
print(data_angka)

data_str = ["Odah", "Mimin", "Surotong"]
print(data_str)

data_bool = [True, False, False, True]
print(data_bool)

data_campuran = ["Tiswan", 20, True]
print(data_campuran)

data_range = (0, 10, 2)
print(data_range)

data_list = list(data_range)
print(data_list)

list_for = [i for i in range(1, 10)]
print(list_for)

list_for_if = [i for i in range(1, 20) if i % 5 != 0]
print(list_for_if)
