#Program konversi temperatur
print ("/n Program Konversi Tempertur Fahrenheit")
celcius = float(input("Masuhakan suhu dalam Fahrenheit : "))

#Reamur 
reamur = (4 / 5) * celcius
print ("Suhu dalam reamur     : ", reamur, 'Reamur')

#Fahrenheit
fahrenheit = ((9 / 5) * celcius) + 32
print ("Suhu dalam Fahrenheit : ", fahrenheit,'F')

#Kelvin 
kelvin = celcius + 273
print ("Suhu dalam Kelvin     : ", kelvin, 'K')

